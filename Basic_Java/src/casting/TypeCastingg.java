package casting;

public class TypeCastingg {

	public static void main(String[] args) {
		
		//CASTING
		
		//IMPLICIT CASTING
		int price=240;
		double fp= price + 70.25;
		System.out.println(fp);
		
		//IMPLICIT CASTING
		int price1=240;
		int fp1= price1 + (int)70.25;
		System.out.println(fp1);
		

	}

}
