package operators;

public class ArithematicOperator {

	public static void main(String[] args) {
		
		//ARITHEMATIC OPERATOR
		
		int a=5;
		int b=5;
		
		int c=20;
		int d=10;
		
		double e=25d;
		double f=6d;
		
		float g=1.25f;
		float h=1.25f;
		
		double i=5d;
		double j=3d;
		
		System.out.println(a+b);
		System.out.println(c-d);
		System.out.println(e/f);
		System.out.println(g*h);
		System.out.println(i%j);

	}

}
